REQUIRED_BY_TYPE = {
    "deployment":     ["name"],
    "service":        ["name"],
    "route":          ["name"],
    "hpa":            ["name"],
    "pdb":            ["name"],
    "statefulset":    ["name"],
    "ingress":        ["name", "host"],
    "pvc":            ["name"],
    "job":            ["name"],
    "serviceaccount": ["name"],
    "configmap":      ["name"],
    "cronjob":        ["name"],
    "externalsecret": ["name"],
    "networkpolicy":  ["name"],
    "docker-ci":      ["image", "registry"],
    "sast-ci":        ["project-key", "sonar-host"],
}


def validate_required(args) -> None:
    missing = []
    for field in REQUIRED_BY_TYPE.get(args.type, []):
        attr = field.replace("-", "_")
        if not getattr(args, attr, ""):
            missing.append(f"--{field}")
    if missing:
        sys.exit(f"ERROR: The following arguments are required for --type {args.type}: {', '.join(missing)}")

    # Validate Kubernetes names
    if args.type not in ("docker-ci", "sast-ci") and args.name:
        validate_k8s_name(args.name)


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main() -> None:
    parser = build_parser()
    args = parser.parse_args()

    validate_required(args)

    # Resolve output directory
    cfg = TYPE_CONFIGS.get(args.type)
    default_dir = cfg["default_dir"] if cfg else "gitops/templates"
    output_dir = guard_output_dir(args.output_dir or default_dir)

    # NetworkPolicy is fully programmatic
    if args.type == "networkpolicy":
        content = traceability_header("networkpolicy") + build_networkpolicy(args)
        out_path = os.path.join(output_dir, f"{args.name}-networkpolicy.yaml")
        write_or_dry_run(out_path, content, args.dry_run, args.force)
        return

    # Template-based types
    template_dir = find_template_dir()
    tpl_raw = load_template(template_dir, cfg["template"])
    replacements = cfg["markers"](args)
    content = render(tpl_raw, replacements)

    out_filename = cfg["output"](args)
    out_path = os.path.join(output_dir, out_filename)

    guard_unreplaced_markers(content, out_path)
    content = traceability_header(args.type) + content

    write_or_dry_run(out_path, content, args.dry_run, args.force)

    # Values hint
    if cfg.get("values_hint") and not args.dry_run:
        print()
        for line in cfg["values_hint"](args):
            print(line)


if __name__ == "__main__":
    main()
