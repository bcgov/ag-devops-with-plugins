#!/usr/bin/env python3
"""
ag-devops unified scaffold CLI - v2.0.0

Generates Helm template fragments and CI/CD configs from BC Gov AG templates.
All resource types are handled by this single script via the --type flag.

Usage:
    python scaffold.py --type deployment --name web-api --port 8080 [--output-dir gitops/templates]
    python scaffold.py --type networkpolicy --name web-api --ingress-from-router --egress-to-apps postgresql:5432
    python scaffold.py --type docker-ci --image my-app --registry ghcr.io/my-org [--output-dir .github/workflows]
    python scaffold.py --type deployment --name web-api --dry-run        # preview only
    python scaffold.py --type service --name web-api --force             # overwrite existing

Safety features:
    - Post-render validation: fails if any @@MARKER@@ remains unreplaced
    - Path traversal guard: --output-dir must be within the working directory
    - Idempotency: skips existing files unless --force is given
    - Kubernetes name validation: names must match ^[a-z][a-z0-9-]{1,62}$
    - Dry-run mode: prints what would be written without touching the filesystem
"""

import argparse
import os
import re
import sys

PLUGIN_VERSION = "2.0.0"

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def to_camel(name: str) -> str:
    """Convert kebab-case or snake_case to camelCase (Helm values key)."""
    parts = re.split(r"[-_]", name)
    return parts[0] + "".join(p.capitalize() for p in parts[1:])


def validate_k8s_name(name: str) -> None:
    """Fail fast if name does not meet Kubernetes naming rules."""
    if not re.match(r"^[a-z][a-z0-9-]{1,62}$", name):
        sys.exit(
            f"ERROR: '{name}' is not a valid Kubernetes name. "
            "Must start with a lowercase letter, contain only [a-z0-9-], and be 2–63 characters."
        )


def guard_output_dir(output_dir: str) -> str:
    """Resolve and validate that output_dir is inside the current working directory."""
    cwd = os.path.realpath(os.getcwd())
    resolved = os.path.realpath(os.path.abspath(output_dir))
    try:
        common = os.path.commonpath([cwd, resolved])
    except ValueError:
        common = ""
    if common != cwd:
        sys.exit(
            f"ERROR: --output-dir '{output_dir}' resolves to '{resolved}' which is outside "
            f"the current working directory '{cwd}'. Refusing to write."
        )
    return resolved


def find_template_dir() -> str:
    """Locate the assets/templates directory relative to this script (handles symlinks)."""
    script_dir = os.path.dirname(os.path.realpath(__file__))
    # Try plugin root layout: scripts/ is sibling of assets/
    candidate = os.path.join(script_dir, "..", "assets", "templates")
    if os.path.isdir(candidate):
        return os.path.abspath(candidate)
    sys.exit(
        f"ERROR: Cannot find assets/templates directory relative to script at '{script_dir}'. "
        "Ensure the plugin structure is intact."
    )


def load_template(template_dir: str, filename: str) -> str:
    path = os.path.join(template_dir, filename)
    if not os.path.isfile(path):
        sys.exit(f"ERROR: Template not found: {path}")
    with open(path, encoding="utf-8") as fh:
        return fh.read()


def render(tpl: str, replacements: dict) -> str:
    for marker, value in replacements.items():
        tpl = tpl.replace(marker, str(value))
    return tpl


def guard_unreplaced_markers(content: str, output_path: str) -> None:
    """Fail if any @@MARKER@@ placeholders survive rendering."""
    remaining = re.findall(r"@@\w+@@", content)
    if remaining:
        unique = sorted(set(remaining))
        sys.exit(
            f"ERROR: Rendering produced unreplaced markers in '{output_path}': {unique}\n"
            "This means required arguments were not provided. Run with --help to see all options."
        )


def traceability_header(resource_type: str) -> str:
    return (
        f"# Generated by ag-devops v{PLUGIN_VERSION} - scaffold.py --type {resource_type}\n"
        "# DO NOT EDIT MANUALLY - re-run scaffold.py to regenerate\n"
    )


def write_or_dry_run(output_path: str, content: str, dry_run: bool, force: bool) -> None:
    if dry_run:
        print(f"[DRY-RUN] Would write: {output_path}")
        print("--- content preview ---")
        print(content[:800] + ("..." if len(content) > 800 else ""))
        print("--- end preview ---")
        return

    if os.path.exists(output_path) and not force:
        print(f"SKIPPED (already exists): {output_path}  -- use --force to overwrite")
        return

    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    with open(output_path, "w", encoding="utf-8") as fh:
        fh.write(content)
    print(f"Written: {output_path}")


