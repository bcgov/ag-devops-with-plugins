def build_networkpolicy(args) -> str:
    """Build NetworkPolicy Helm template content from CLI flags."""
    lines = [
        '{{- $np := dict "Values" .Values -}}',
        '{{- $_ := set $np "ApplicationGroup" .Values.project -}}',
        f'{{{{- $_ := set $np "Name" "{args.name}" -}}}}',
        '{{- $_ := set $np "Namespace" $.Release.Namespace -}}',
    ]

    # Policy types
    has_ingress = args.ingress_from_router or bool(args.ingress_from_apps)
    has_egress = bool(args.egress_to_apps) or bool(args.egress_to_cidr) or args.egress_internet
    policy_types = " ".join(
        f'"{t}"' for t in (["Ingress"] if has_ingress else []) + (["Egress"] if has_egress else [])
    ) or '"Ingress"'
    lines.append(f'{{{{- $_ := set $np "PolicyTypes" (list {policy_types}) -}}}}')

    # Internet egress annotations
    if args.egress_internet:
        if not args.justification or not args.approved_by:
            sys.exit("ERROR: --justification and --approved-by are required with --egress-internet")
        lines.append(
            f'{{{{- $_ := set $np "Annotations" (dict\n'
            f'  "justification" "{args.justification}"\n'
            f'  "approvedBy"    "{args.approved_by}"\n'
            f') -}}}}'
        )

    # AllowIngressFrom block
    if has_ingress:
        app_names = [a.strip() for a in args.ingress_from_apps.split(",") if a.strip()] if args.ingress_from_apps else []
        block = ['{{- $_ := set $np "AllowIngressFrom" (dict', f'  "ports" (list {args.port})']
        if app_names:
            items = "\n".join(f'    (dict "name" "{a}")' for a in app_names)
            block.append(f'  "apps" (list\n{items}\n  )')
        if args.ingress_from_router:
            block.append(
                '  "namespaces" (list (dict\n'
                '    "name" "openshift-ingress"\n'
                '    "podSelector" (dict "matchLabels" (dict\n'
                '      "ingresscontroller.operator.openshift.io/deployment-ingresscontroller" "default"\n'
                '    ))\n'
                '  ))'
            )
        block.append(") -}}")
        lines.append("\n".join(block))

    # AllowEgressTo block
    if has_egress:
        app_pairs, cidr_entries = [], []
        if args.egress_to_apps:
            for pair in args.egress_to_apps.split(","):
                pair = pair.strip()
                if ":" not in pair:
                    sys.exit(f"ERROR: --egress-to-apps value '{pair}' must be name:port")
                app_name, port = pair.rsplit(":", 1)
                app_pairs.append((app_name.strip(), int(port.strip())))
        if args.egress_to_cidr:
            for entry in args.egress_to_cidr.split(","):
                entry = entry.strip()
                if ":" not in entry:
                    sys.exit(f"ERROR: --egress-to-cidr value '{entry}' must be cidr:port")
                cidr, port = entry.rsplit(":", 1)
                cidr_entries.append((cidr.strip(), int(port.strip())))

        block = ['{{- $_ := set $np "AllowEgressTo" (dict']
        if app_pairs:
            items = "\n".join(
                f'    (dict\n      "name" "{n}"\n      "ports" (list (dict "port" {p} "protocol" "TCP"))\n    )'
                for n, p in app_pairs
            )
            block.append(f'  "apps" (list\n{items}\n  )')
        if cidr_entries:
            items = "\n".join(
                f'    (dict\n      "cidr" "{c}"\n      "ports" (list {p})\n    )'
                for c, p in cidr_entries
            )
            block.append(f'  "ipBlocks" (list\n{items}\n  )')
        if args.egress_internet:
            block.append(
                '  "internet" (dict\n    "enabled" true\n    "cidrs" (list "0.0.0.0/0")\n    "ports" (list 443)\n  )'
            )
        block.append(") -}}")
        lines.append("\n".join(block))

    lines.append('{{ include "ag-template.networkpolicy" $np }}')
    return "\n".join(lines) + "\n"


