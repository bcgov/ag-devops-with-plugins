"""
Test harness for ag-devops scaffold.py

Verifies that `scaffold.py --type X` produces:
  1. Valid YAML (parseable by PyYAML)
  2. No unreplaced @@MARKER@@ tokens
  3. Expected Helm/content fragments per type
  4. Correct output filename and default output directory

Run with:
    pytest tests/ -v
    pytest tests/ -v --update-fixtures   # regenerate golden files
"""

import os
import re
import subprocess
import sys
import shutil
import tempfile
from pathlib import Path

import pytest
import yaml

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

REPO_ROOT = Path(__file__).parent.parent.resolve()
SCAFFOLD = REPO_ROOT / "plugins" / "ag-devops" / "scripts" / "scaffold.py"
FIXTURES = Path(__file__).parent / "fixtures" / "expected"
# Test output dirs go inside the repo to satisfy the path traversal guard
TEST_OUTPUT_BASE = Path(__file__).parent / "output"

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

@pytest.fixture(autouse=True)
def in_repo_root():
    """Ensure CWD is the repo root for all tests (path traversal guard requires this)."""
    old = os.getcwd()
    os.chdir(REPO_ROOT)
    yield
    os.chdir(old)


@pytest.fixture
def scaffold_out_dir(request):
    """Create a unique output dir relative to REPO_ROOT (satisfies path traversal guard)."""
    test_name = re.sub(r"[^a-zA-Z0-9_-]", "_", request.node.name)[:50]
    rel = Path("tests") / "output" / test_name
    abs_d = REPO_ROOT / rel
    abs_d.mkdir(parents=True, exist_ok=True)
    yield rel  # yield RELATIVE path so scaffold.py resolves it against its own cwd=REPO_ROOT
    shutil.rmtree(abs_d, ignore_errors=True)


def run_scaffold(*extra_args, expect_fail=False, output_dir=None):
    """Run scaffold.py with given args; return (stdout, stderr, returncode)."""
    cmd = [sys.executable, str(SCAFFOLD)]
    if output_dir:
        cmd += ["--output-dir", str(output_dir)]
    cmd += list(extra_args)
    result = subprocess.run(cmd, capture_output=True, text=True, cwd=str(REPO_ROOT))
    if not expect_fail and result.returncode != 0:
        pytest.fail(
            f"scaffold.py failed (rc={result.returncode}):\n{result.stdout}\n{result.stderr}"
        )
    return result.stdout, result.stderr, result.returncode


def read_output(output_dir, filename):
    """Read the generated file from output_dir (relative to REPO_ROOT)."""
    path = REPO_ROOT / output_dir / filename
    assert path.exists(), f"Expected output file not found: {path}"
    return path.read_text(encoding="utf-8")


def assert_no_markers(content, filename=""):
    """Assert no @@MARKER@@ tokens remain in rendered content."""
    remaining = re.findall(r"@@\w+@@", content)
    assert not remaining, f"Unreplaced markers in {filename}: {remaining}"


def assert_valid_yaml(content, filename=""):
    """Assert content is parseable YAML (may be multi-document)."""
    try:
        list(yaml.safe_load_all(content))
    except yaml.YAMLError as exc:
        pytest.fail(f"Invalid YAML in {filename}:\n{exc}\n\nContent:\n{content}")


def save_or_compare_fixture(name, content, update):
    """Save as golden fixture or compare against existing."""
    fixture_path = FIXTURES / name
    if update or not fixture_path.exists():
        FIXTURES.mkdir(parents=True, exist_ok=True)
        fixture_path.write_text(content, encoding="utf-8")
        print(f"  [fixture saved] {fixture_path.name}")
    else:
        expected = fixture_path.read_text(encoding="utf-8")
        # Strip traceability header lines for comparison (they contain version)
        def strip_header(s):
            lines = s.splitlines(keepends=True)
            return "".join(l for l in lines if not l.startswith("# Generated by ag-devops") and not l.startswith("# DO NOT EDIT"))
        assert strip_header(content) == strip_header(expected), (
            f"Output differs from fixture {fixture_path.name}.\n"
            "Re-run with --update-fixtures to regenerate."
        )


